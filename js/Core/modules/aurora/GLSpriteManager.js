export class GLSpriteManager
{
	constructor()
	{
		this.spriteInfo = [];
	}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	Add(i, texture, data)
	{
		this.spriteInfo[i] = {texture: texture, data:data};
	}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	GetSpriteByIndex(index)
	{
		return this.spriteInfo[index];
	}
}