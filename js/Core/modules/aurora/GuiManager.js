export class GuiManager
{
	constructor()
	{
		this.gui = [];
	}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	Add(i, gui)
	{
		this.gui[i] = gui;
	}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	GetGui(index)
	{
		return this.gui[index];
	}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

	Update(index, deltaTime)
	{
		this.gui[index].Update(deltaTime);
	}
}